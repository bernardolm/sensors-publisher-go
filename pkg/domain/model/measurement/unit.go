package measurement

// UnitOfMeasurement identifies the measurement unit stored with one value.
type UnitOfMeasurement string

const (
	UnitOfMeasurementHectopascal       UnitOfMeasurement = "hPa"
	UnitOfMeasurementPascal            UnitOfMeasurement = "Pa"
	UnitOfMeasurementKilopascal        UnitOfMeasurement = "kPa"
	UnitOfMeasurementBar               UnitOfMeasurement = "bar"
	UnitOfMeasurementPSI               UnitOfMeasurement = "psi"
	UnitOfMeasurementCelsius           UnitOfMeasurement = "C"
	UnitOfMeasurementFahrenheit        UnitOfMeasurement = "F"
	UnitOfMeasurementKelvin            UnitOfMeasurement = "K"
	UnitOfMeasurementPercent           UnitOfMeasurement = "%"
	UnitOfMeasurementMillimeterMercury UnitOfMeasurement = "mmHg"
)
